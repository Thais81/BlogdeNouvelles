    @Override
    public Utilisateur read(Integer id) {
        Utilisateur obj = null;
        String sql = "SELECT * FROM Utilisateur WHERE id_Utilisateur=?";
        PreparedStatement pstmt;
        try {
            pstmt = connexion.prepareStatement(sql);
            pstmt.setInt(1, id);
            ResultSet rs = pstmt.executeQuery();
            if (rs.first()) {
                obj = new Utilisateur();
                obj.setId(rs.getInt("id_Utilisateur"));
                obj.setPseudo(rs.getString("pseudo"));
                obj.setMot_de_passe(rs.getString("mot_de_passe"));
                obj.setEstActif(rs.getBoolean("estActif"));
            }
        } catch (SQLException ex) {
            System.err.println("Erreur lors de la lecture : " + ex.getMessage());
        }
        return obj;
    }

